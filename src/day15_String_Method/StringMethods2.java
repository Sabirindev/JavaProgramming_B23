package day15_String_Method;

public class StringMethods2 {
    public static void main(String[] args) {

        //************substring()**************
        System.out.println("*******String substring() Method********");

        String sentence0 = "Java is a Programming Languag";
        //index            0123

        String sentence1 = sentence0.substring(0, 3 + 1);
        System.out.println(sentence1);

        //******************************
        String s = "Cybertek School";
        //          0123456789...
        String s1 = s.substring(9,15);
        System.out.println("s1 = "+s1);

        //*****************************

        String s2 = "Scbertek Campus";
        //index      0123456789..
        String s3 = s2.substring(9);
        System.out.println("s3 = "+s3);

        //****************

        String s4 = "Java is a Programming Language";

        String s5 = s4.substring(10);
        System.out.println("s5 = "+s5);

        //**********replace()*********************
        System.out.println("***********String replace() Method********");

        String sentence3 = "Python is an easy language";
        sentence3 = sentence3.replace("Python","Java");
        System.out.println(sentence3);

        String sentence4 = "C# is a programming language, I like learning C#, C# is the best";
        sentence4 = sentence4.replace("C#","Java");
        System.out.println(sentence4);

        String sentence5 = "Dog is friendly, Dog is loyal, I love Dogs";
        sentence5 = sentence5.replace("Dog","Cat");
        System.out.println(sentence5);

        //************************
        String str = "Batch 23";
        //index       01234567

        char ch1 = str.charAt(0);
        System.out.println("ch1 = "+ch1);

        str = str.toUpperCase();
        System.out.println(str);

        str = str.toLowerCase();
        System.out.println(str);

        String str2 = "Cybertek is a great school";
        String s6 = str2.substring(14,19);
        System.out.println(s6);



    }
}
