package day19_ForLoop;

public class Rectangle_ForLoop {
    public static void main(String[] args) {
        for (int j = 0; j <=7; j++) {
            for (int i = 0; i <= 5; i++) {
                System.out.print("* ");
            }
            System.out.println("");
        }
    }
}
